function randomCard(){
    if (needPast === false && needPresent === false && needFuture === false) {
        return;
    }
  let randomNum = Math.floor(Math.random()*cards.length)
  while (cardsRemaining[randomNum].cardUsed === true) {
      randomNum = Math.floor(Math.random()*cards.length)
  }
  cardsRemaining[randomNum].cardUsed = true
  if (needPast){
      needPast = false
      if (randomBoolean()){
        pastTitle.textContent = cardsRemaining[randomNum].name
        pastImg.src = cardsRemaining[randomNum].img
        pastMeaning.textContent = cardsRemaining[randomNum].revMeaning
        pastName.textContent = 'Past'
        pastImg.classList.add('reverse')
        past.style.display = 'flex'

      } else {
        pastTitle.textContent = cardsRemaining[randomNum].name
        pastImg.src = cardsRemaining[randomNum].img
        pastMeaning.textContent = cardsRemaining[randomNum].meaning
        pastName.textContent = 'Past'
        past.style.display = 'flex'
      }

  } else if (needPresent){
      needPresent = false
      if (randomBoolean()){
        presentTitle.textContent = cardsRemaining[randomNum].name
        presentImg.src = cardsRemaining[randomNum].img
        presentMeaning.textContent = cardsRemaining[randomNum].revMeaning
        presentName.textContent = 'Present'
        presentImg.classList.add('reverse')
        present.style.display = 'flex'
      } else {
        presentTitle.textContent = cardsRemaining[randomNum].name
        presentImg.src = cardsRemaining[randomNum].img
        presentMeaning.textContent = cardsRemaining[randomNum].meaning
        presentName.textContent = 'Present'
        present.style.display = 'flex'
      }

  } else if (needFuture ){
      needFuture = false
      if (randomBoolean()){
        futureTitle.textContent = cardsRemaining[randomNum].name
        futureImg.src = cardsRemaining[randomNum].img
        futureMeaning.textContent = cardsRemaining[randomNum].revMeaning
        futureName.textContent = 'Future'
        futureImg.classList.add('reverse')
        future.style.display = 'flex'
      } else {
        futureTitle.textContent = cardsRemaining[randomNum].name
        futureImg.src = cardsRemaining[randomNum].img
        futureMeaning.textContent = cardsRemaining[randomNum].meaning
        futureName.textContent = 'Future'
        future.style.display = 'flex'
      }
  }
}
